const sendEvent = (category: string, action: string, label: string = '', value: number = 0) => {
    if (typeof window !== 'undefined' && (window as any).gtag) {
      (window as any).gtag('event', action, {
        event_category: category,
        event_label: label,
        value: value,
      });
    }
  };

  export default sendEvent